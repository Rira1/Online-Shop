    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
                try {
                    BufferedWriter bfw = new BufferedWriter(new FileWriter("D:\\Study stuff\\CSC305\\Online Grocery Shop\\Admin data\\Data.txt"));
            for(int i = 0 ; i < jtab.getColumnCount() ; i++)
  {
    bfw.write(jtab.getColumnName(i));
    bfw.write("\t");;
  }

  for (int i = 0 ; i < jtab.getRowCount(); i++)
  {
    bfw.newLine();
    for(int j = 0 ; j < jtab.getColumnCount();j++)
    {
      bfw.write((String)(jtab.getValueAt(i,j)));
      bfw.write("\t");;
    }
  }
  bfw.close();
        } catch (IOException ex) {
            Logger.getLogger(Adminn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jtabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtabMouseClicked
         DefaultTableModel model = (DefaultTableModel)jtab.getModel() ;
        jtt.setText(model.getValueAt(jtab.getSelectedRow(), 0).toString());
        jtt1.setText(model.getValueAt(jtab.getSelectedRow(), 1).toString());
        cb.setSelectedItem(model.getValueAt(jtab.getSelectedRow(), 2).toString());
        jtt2.setText(model.getValueAt(jtab.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_jtabMouseClicked

